#include "minitalk.h"

int	main(void)
{
	int	a;
	int	b;

	a = 1; // 0000 0001
	b = a << 8;
	printf("b = %d\n", b);
	b = b >> 8;
	printf("b = %d\n", b);
	return (0);
}
